                   <p class="mb-0">SERB Sponsored Seminar</p>
                     <!--<b class="mb-0 fontb">SERB Sponsored Seminar</b>-->
                     <div class="overviewtextwrap">
                        <p class="mt-4 mb-2 fmn">Overview of the Programme :</p>
                      	<p>In the <b>Inaugural Session on Day 1, 9th June 2022</b>, the program was inaugurated with lighting of the lamp, SaraswatiVandana, followed by Key Note Speeches by the <b>Chief Guests</b> of the session, <b>Prof. (Dr.) Rajendra B. Kakde</b>, Advisor I, AICTE, New Delhi, <b>Dr. Atul Nasa</b>, Deputy Drug Controller and Head of Office, Controlling & Licensing Authority, Drugs Control Department, New Delhi,and Welcome Addresses by the hon’ble Executive Vice President of NIET, <b>Shri Raman Batra</b>, and <b>Dr. Avijit Mazumder</b>, Director, NIET Pharmacy Institute. The Inaugural Session was concluded by the Vote of Thanks, followed by the National Anthem and then a short interval for the Morning Breakfast and Tea.</p>                        
                      	<p>The <b>Technical Session - I</b> was enlighted by the expert lecture by <b>Dr. Bibhu Prasad Panda</b>, Associate Professor, Department of Pharmacy, Jamia Hamdard, New Delhi, who delivered a very informative and brilliant speech on <b>“Development of Drug-Resistant Cells: Its Causes and Solutions”</b>.In his lecture Dr. Panda described the root causes of developing drug resistance, highlighting the development and spreading of drug resistance at the molecular and genetic levels, in bacterial, viral as well as human cells. He also emphasized on the effect of drug resistance on the dreadful spreading of the tumor cells and development of drug resistant Covid-19 mutants. He concluded his speech by explaining the continuing research on drug resistance, genetic mutation, and search for natural bioactives and novel synthetic compounds, active against the drug resistant strains, as the sole solution to overcome the acute problem of drug resistance.</p>
                      	<p>In the <b>Technical Session - II, Dr. Vivekanandan Kalaiselvan</b>,  Senior Principal Scientific Officer, Indian Pharmacopoeia Commission, Ghaziabad, discussed on drug resistance and roles of the drug regulatory bodies associated with drug distribution,pharmacovigilance. He emphasized in his lecture, the adverse manifestations of the misuse of drugs, self-medication, over the counter medications, improper disposal mechanisms, and the role of the pharmacists to control the same.</p>
                      	<p>The <b>Technical Session - II</b> was followed bythe <b>Oral Presentation Session</b>, where 6 participantspresented their topics through power point presentations, and <b>Dr. Sushma Verma</b>, Professor, NIET Pharmacy Institute,<b>Dr. Salahuddin</b>, Associate Professor, NIET Pharmacy Institute, and <b>Dr. Rashmi Mishra</b>, HOD,Dept. of Biotechnology, NIET, were the Judges. </p>
                      	<p>The <b>Day 1</b> of the 3 days’ long seminar ended with <b>tea and evening snacks</b>. <br/> All the events of <b>Day 1</b> were anchored systematically by <b>Dr. Sweta Bawari</b>, Associate Professor, and <b>Mr. Abhijit Debnath</b>, Asst. Professor of the NIET Pharmacy Institute, Greater Noida.</p>
                      	<p>The <b>Day 2</b> events started with Morning Tea and Breakfast at 09:30 am on 10th June 2022, followed by the expert lecture of <b>Technical session - IIIby Dr. Avijit Mazumder, Director</b>, NIET Pharmacy Institute, and Convener of the SERB sponsored 3 days’ seminar, who elaborated very clearly and simply in his lecture entitled, <b>"Antibiotic Resistance: Causes and Challenges for Digital India"</b>, the probable causes and mechanisms of antibiotic resistance, significance of the rational use of antibiotics, effects of expired and unused drugs on the atmosphere, the concept of antibiograms, and modern approaches to control drug resistance.</p>
                      	<p>The <b>Technical session - IV</b> onthe <b>Day 2</b> was glorified bythe presence of a legendary figure from the pharma software domain, <b>Dr. Gajendra Pal Singh Raghava</b>, Professor, Indraprastha Institute of Information Technology (IIIT), New Delhi, with his expert lecture entitled, <b> “Computational Resources for Managing Drug Resistance”</b>, where he enlightened the audience with his vast knowledge and experience on the emerging and novel concepts on protein / peptide based therapies, vaccines and immunotherapy, phage based therapy, the comfort and significance of using Open Source Software,and how to use various web services for designing therapeutic molecules in the healthcare system. <b>Dr. Raghava’s speech</b> had really driven our thought process in newer directions and helped us to think on the use of various computational approaches in overcoming the problems associated with drug resistance and drug delivery.</p>
                        <p>The <b>Lunch Break</b> after the <b>Technical session IV</b> of <b>Day 2</b> was followed by the <b>Technical Session – V</b>, where an eminent researcher and Professor, <b>Dr. Reeta K.</b>, from the Department of Pharmacology, All India Institute of Medical Sciences (AIIMS), New Delhi, spoke on the topic entitled, <b>“Antimicrobial Resistance: Challenges and Approaches”</b>. In her expert lecture,<b> Dr. Reeta</b> highlighted the various aspects of antimicrobial stewardnessprogrammes which are under consideration by the Government to control the enigma of drug resistance. She also recommended the factors to be considered by the physicians while prescribing antibiotics to the patients.</p>
                        <p>The <b>Technical Session – V</b> was followed by the Poster Presentation Session, where 8 out of the 19 short listed candidates presented their e-posters to explain their topics.  </p>
                        <p>The various events of <b>Day 2</b> were anchored systematically by <b>Mrs. Anjali Bipin Mishra, Asst. Professor</b>, and <b>Ms Pratishttha Bharadwaj</b>, Asst. Professor of the NIET Pharmacy Institute, Greater Noida.</p>
                        <p>The <b>Day 3</b> of the 3 days’ seminar began with the <b> Morning Breakfast and Tea Session</b> ,followed by the <b>Technical Session – VI</b> with the expert lecture by <b>Dr. Manju Sharma</b>, Professor, Department of Pharmacy, JamiaHamdard, New Delhi, on the topic, <b>“Pharmacovigilance: Current approach to Control Drug Resistance”</b>. <b>Dr. Sharma</b> in her very informative speech explained the roles of pharmacovigilance to control the ever increasing trend of drug resistance and the various approaches to overcome the menace.</p>
                        <p>The lecture of <b>Dr. Manju Sharma</b> was followed by the <b>Technical Session – VII</b>, with the 7th lecture of the seminar by <b>Dr. Urmi Bajpai</b>, Associate Professor, ANDC College, Delhi University, New Delhi, on <b>“Antimicrobial Resistance: A Silent Pandemic”</b> , where <b>Dr. Bajpai</b> explained very nicely and elaborately the various causes of antimicrobial resistance and focused on the research on bacteriophages to use them as antimicrobial agents to develop bacteriophages-based therapy in future.</p>
                        <p>The <b>Technical Session – VII</b> of the seminar was followed by the remaining part of the <b>e-poster presentations</b> and thereafter the <b>lunch break</b>.</p>
                        <p>All the events of the <b>Day 3</b> were anchored systematically by <b>Ms. Deepika Pathak</b>, and <b>Mr. Abhijit Debnath</b> Asst. Professors of the NIET Pharmacy Institute, Greater Noida.</p>
                        <p>Altogether, there was spellbound participation of all the participants, as well as the speakers throughout the 3 days’ long DST-SERB sponsored seminar. All the participants have enjoyed the lectures and learnt many new topics and technologies, which they may apply in their teaching-learning processes to make those interesting and attractive and hope these will result in an extreme benefit for their students and colleagues.</p>
                      	
