                           <div class="tab-pane fade" id="Canteen" role="tabpanel" aria-labelledby="canteen-tab">
                             <strong>WOMEN GRIEVANCE REDRESSAL CELL</strong>
                             <div class="dpharmtextwrap">
                             <strong>Rule Against Sexual Harassment</strong>
                             <b class="mt-3">1. Background</b>
                             <span> A. Sexual harassment is a human rights violation, an infringement on life and liberty and a serious form of gender-based discrimination. Such behaviour is an affront to dignity, gender, equality, and fundamental rights.</span>
							 <span> B. Sexual harassment is contrary to anti-discrimination provisions in the Constitution of India:Article 15: Prohibition of discrimination on the grounds of religion, race, caste, sex, or place of birth and Article 19(1)(g): Right to freedom which upholds a woman’s right to practice any profession, or to carry on any occupation, trade or business.</span>
                             <span> C. Sexual Harassment is an offence under The Sexual Harassment of Women at Workplace(Prevention, Prohibition, and Redressal) Act, 2013 No. 14 of 2013. Section 3(1):No woman shall be subjected to sexual harassment at any workplace.</span>
                             <span> D. Educational Institutes are bound by the same Act (Section 2 (o) workplace includes - (ii) any private sector organization or a private venture, undertaking ,enterprise, institution, establishment, society, trust, non-governmental organisation, unit or service provider carrying on commercial distribution or service. Following this, NIET (Pharmacy Institute) is committed to uphold the constitutional mandate ensuring the above-mentioned human rights of all those who fall within its jurisdiction. It is with this object that these rules have been framed.</span>
                             <div class="clearfix"></div>
                               
                             <b class="mt-4">2. Objectives and Scope of the Rules against Sexual Harassment</b>
							  <span>A. These rules restate the commitment of NIET (Pharmacy Institute) to create and maintain a community suitable, i.e. free of all sexual harassment, for all its member so that they can work together in harmony.</span>
							  <span>B. In framing the rules and procedures, the Spirit of Sexual Harassment of Women at Workplace Act 2013, and the Supreme Court judgement have been followed. These Rules and Procedures apply to all the students, academic staffs, faculty members, non-teaching staffs, officers of NIET(Pharmacy Institute), the members of the authorities and the committee of NIET (Pharmacy Institute)as well as to service providers and outsiders.</span>
							  <span>C. In order to implement these Rules, a Women Grievances Redressal Cell (WGRC) shall be appointed whose composition is described below.</span>
							  <div class="clearfix"></div>
                               
							<b class="mt-4">3. Definitions of Sexual Harassment</b>
							<span>A. Sexual Harassment in the given context is described in The Sexual Harassment of Women atthe Workplace Act 2013 in Paragraph 2(n) as: “sexual harassment” includes any one of the following unwelcome act or behaviour.</span>
                             <ul>
                              <li>I. Physical contacts and advances;or</li>
                              <li>II. A demand or request for sexual favours; or</li>
                              <li>III. Making sexually coloured remarks; or</li>
                              <li>IV. Showing pornography;or</li>
                              <li>V. Any other unwelcome physical, verbal or non-verbal conduct of a sexual nature;</li>
                             </ul> 
                              <span>B. Within NIET (Pharmacy Institute), the definition of the above applies to both men and women and also includes harassment by a member of another member of the same sex.</span>
                              <span>C. The key expression in the above definition is ‘unwelcome’ which indicates the unwanted and non-consensual nature of the behavior in question.</span>
                              <span>D. The overwhelming dominant form of sexual harassment is committed by men against women. However, it could also be committed by women against men or occur between persons of thesamegender.</span>
								 <div class="clearfix"></div>
                               
                               	<strong class="mt-4">Gender Sensitization</strong>
                                <p>This works in creating awareness about gender issues and working towards creating an equilibrium where both men and women can work together with a sense ofpersonalsecurityanddignity.</p>
                                
                                <strong class="mt-4">Women Grievance Redressal Cell(WGRC)</strong>
                                <p>In order to maintain safety and security for the girl students and women employees, a cell hasbeen constituted for redressal of grievances. The WGRC stays alert and active to prevent anysexual abuse towards the students and female workers. If the students face any harassment on thecampus, they can complain to the women grievance cell. The cell members look into the issues, gather the evidence, and take necessary action against the guilty.</p>
                                <p>Director / Deans / HODs also assist regularly in attending to these problems. The cell maintains the records of the grievances ,actions recommended and taken and the settlement of the grievances. The cell is headed by a senior lady faculty member ( Convenor) and six facultymembers, who form the core group. The Director and the Proctor are the ex-officio members ofthis Coregroup.</p>
                                <p>Complaints can be made, in writing, to the Director/ Convenor or any of the other members or inthe complaint box placed at strategic locations in the hostel/ outside the WGRC Convener’sOffice. Boxes are opened once a month and checked. Any grievance found in it is scrutinized,and necessary actions are immediately taken by the cell. In case of emergency, the Director  call committee members and the problem are attended to immediately .The cell has resolved all difficulties amicably ever since it was setup in 2012.</p>

								<strong class="mt-4">Duties and Responsibilities</strong>
                               <ul class="ullist">
                                   	<li>To examine all grievance letters/ suggestions/ complaints received from the women staff/ students regarding the sexual harassment.</li>
                                    <li>To give feedback to the women staff/students concerned/ to find solution for their grievances. </li>
                                    <li>The committee will record such grievances received from the women staff / students, in a separate register maintained exclusively for this purpose. The reply given by the committee to the women staff / students for grievance should also be recorded in the register.</li>
                                    <li>The Committee should find suitable solutions to settle the problems faced by the women staff / students in regard to matters relating to sexual harassment within 10 days' after thorough investigation.</li>
                               </ul>
   
