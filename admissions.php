                      <div class="tab-pane fade mbpharm" id="faq" role="tabpanel" aria-labelledby="profile-tab">
<strong>Frequently Asked Questions regarding Admissions</strong>
                        <p><b>How to Apply ? </b></p>
<p>
After getting the information regarding the courses and fulfilling the eligibility criteria, the student can apply by appearing for the Admission process.
</p>

<p><b>How does the college admission process work?</b></p>
<p>
The college takes admission from 2 sources:<br>
<b>1.	UPSEE Counseling:</b> The student needs to appear for the UPSEE exam and appear in the online counseling. There he / she needs to opt NIET Pharmacy Instt as the preferred choice in with 908 College code for BPharm and MPharm under AKTU and 1067 college code under UPBTE for DPharm
<br><br>

<b>2.	Direct Admission: </b>The college has its own admission test which comprises of<br>
a.	Objective type Aptitude / English test <br>
b.	Objective type Math/Biology  test for B.Pharm. applications .<br>
c.	Personal Interview After this, your application and test scores are evaluated to ensure that they meet the eligibility criteria set by AKTU. This applies to all courses. An admission committee constituted of senior management and faculty members specializing in your field of study review all your application materials. You receive a letter from the Registrar Office, notifying you of your admission status. If you are found eligible on verification and successfully compete in Selection Round, you will receive the Registration Form to take Provisional Admission on payment of the required fee.</p>

<p><b>What is the basis for an admission decision?</b></p>
<p>
Admission decisions for all the programmes are based on scholastic aptitude, performance score in selection rounds and suitability to take up the desired course provided the candidate meets the eligibility condition as mentioned in the brochures in terms of qualifying aggregate percentage/marks/grades in your till date qualification.
</p>

<p><b>What are the college admission requirements?</b></p>
<p>
A Student applying for admission should carry the original and 2 sets of photocopies of all his testimonials. The documents required are:
</p><ul>
    <li>Adhaar Card</li>
    <li>10th Marksheet</li>
    <li>12th Marksheet</li>
    <li>Graduation degree and marksheets (in case of PG courses)</li>
    <li>Proof of Entrance Exam</li>
    <li>Passport size photographs</li>
    <li>Proof of Gap years (if any)</li>
    
</ul>
                      
<p><b>How can institute support, if the candidate wants to apply for the education loan?</b></p>
<p>
Once the candidate has paid the amount to reserve the seat, the Provisional Admission Certificate is issued by the institution. In case the candidate needs education loan, Bona-fide, Expenditure statement, Placement details, and any other document required by the bank will be provided to him/her by the institution enabling to process for the Education loan.
</p>

                        <p><b>What are the timings for admissions?</b></p>
<p>
NIET Pharmacy Instt remains open for admissions between 9:30 a.m. and 5 p.m. (IST), Monday through Saturday, with the exception of major Indian holidays. However, the admission written exam will not happen after 3:30 p.m.
</p>

                        <p><b>Housing / hostel accomodation</b></p>
<p>
NIET provides completely hygienic separate boys and girls hostel accommodation with all standard civic amenities. Hostel accommodation is mandatory for all the non-residents of Delhi / NCR.
</p>

<p><b>How do i arrange for a campus visit?</b></p>
<p>
Fifteen-minute tours of campus, leaving from the Admission Centre, are available. Tours are held on the hours, Monday through Saturday, from 10 a.m. to 3 p.m. at regular intervals.
</p>

                        
