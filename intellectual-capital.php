                  <strong>DEAN'S PROFILE</strong>
                  <p>Dr (Mrs) Rupa Mazumder is working as Professor and Dean in the NIET Pharmacy Institute in since October 2009. Prof (Mrs.) Mazumder had completed her graduation and post graduation from Department of Pharmaceutical Technology, Jadavpur University. She obtained her PhD (Pharmacy) Degree from Jadavpur University, Kolkata. She began her teaching career as Lecturer in Institute of Pharmacy and Technology, Salipur, Cuttack. Later she was associated with Birla Institute of Technology (BIT), Mesra, Ranchi since 1998 till her joining NMIMS as Professor in Division of Pharmaceutics of School of Pharmacy and Technology Management, NMIMS University, Mumbai. Dr. Mazumder had guided fifty six  post graduate and nine PhD students in pharmacy. Currently a number of students are already pursuing their Ph.D. programme under her guidance in the field of pharmaceutics. She had a total of more than one hundred eighty six national and international publications to her credit. She is a member of various universities in the capacity of PhD thesis evaluators and paper setters and is in board of various international journals. She had contributed ten book chapters. Prof (Mrs ) Mazumder was the founder member of Jharkhand State Pharmacy Tribunal.</p>
