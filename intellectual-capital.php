                  <strong>DIRECTOR'S PROFILE</strong>
                  <p>Dr Avijit Mazumder is working as Professor and Director in Noida Institute of Engineering and Technology Pharmacy Institute since October 2009.Under his guidance, this institute has become the only institute under the affiliating Dr APJ Abdul Kalam Technical University Lucknow to be ranked in NIRF three times in a row and selected amongst top 100 institutes of the country. NIET Pharmacy Institute has also been ranked 49th as per NIRF 2020 as ranked by Ministry of HRD, Govt of India. The undergraduate course of the institute is NBA Accredited since 2017 to 2021 under his leadership. Dr Mazumder had completed his graduation and post graduation from Department of Pharmaceutical Technology, Jadavpur University and then he completed his MBA from University of Lucknow. He obtained his PhD (Pharmacy) Degree from Jadavpur University, Kolkata. He began his teaching career as Lecturer in Institute of Pharmacy and Technology, Salipur, Cuttack. </p>
                  <p>Later he was associated with Birla Institute of Technology (BIT), Mesra, Ranchi since 1999 till his joining NarseeMonjee Institute of Management Studies (NMIMS) Mumbai as Professor and Head in Department of Clinical Pharmacy in November, 2007. He was the Head Division Of Clinical Pharmacy, Chairperson of MSc (Applied Pharmaceutical Sciences) and M.Pharm (Part time Programme) of NMIMS University. He has filed six patents and had received various RPS and MODROB Grants from AICTE, New Delhi. Ten students have been awarded PhD. (Pharmacy) under is direct supervision from government Universities. Dr Mazumder had successfully guided one hundred six post graduate students. Currently eleven students are already awarded their PhD programme under his guidance in the field of Pharmacology, Natural products and Management. He had a total of more than three hundred fifty national and international publications to his credit. Incidentally he had presented a number of research papers in national and international conferences/ symposiums. He had contributed ten book chapters and is the author of three books of repute. Dr Mazumder is in the editorial board of seven international and one national journals. He is also associated with a number of universities like Jadavpur University, AKTU, Utkal University, Rajeev Gandhi Technical University (Bhopal), BIT Ranchi, Dibrugarh University in various capacities.
                  </p>
                  <span>Dr Avijit Mazumder</span>
