                  <strong>DIPLOMA IN PHARMACY (D.PHARM)</strong>
                   <div class="dpharmtextwrap">
                     <strong>PROGRAM INTRODUCTION</strong>
                     <p>Diploma in Pharmacy (D.Pharm) is two years course intended to train students to produce dispensing pharmacists who are to play a vital role in our healthcare system especially in community pharmacy of hospitals and nursing homes. The program covers basic pharmaceutical education including Pharmaceutics, Biochemistry, Pharmacology and Toxicology , Pharmacognosy along with their application in the healthcare Industry. The program holds good value in the Community Pharmacy, Pharmaceutical Industry, Pharmacy Shops, and hospitals.</p>
                     <p>Diploma in Pharmacy is duly approved by AICTE and PCI, and affiliated to Board of Technical Education (BTE), Lucknow, Uttar Pradesh.</p>
                     <strong class="mt-4">MERITS</strong>
                     <ul class="ullist">
                        <li>Experienced and vibrant full-time faculty</li>
                        <li>On hand training for dispensing and manufacturing of pharmaceuticals. </li>
                        <li>Regular interaction with community pharmacy and industrial pharmacy experts</li>
                        <li>Opportunity to use and witness postgraduate and research infrastructure at the undergraduate level</li>
                        <li>Guidance and counseling from faculty members</li>
                        <li>Placement focus in Pharmaceutical industries, Hospitals, Retail Pharmacy in various government departments like Defense, Railways etc. </li>
                     </ul>
                     
                     <strong class="mt-4">CAREER OPTIONS</strong>
                     <p>Diverse job opportunities are available in front of Diploma in Pharmacy certificate holders. Government and private sector job opportunities are available in front of them.</p>
                     <ul class="ullist">
                        <li>Pharmacists in drug stores in Government hospitals, private hospitals, clinics, community health centers or private drug stores.</li>
                        <li>Retail Pharmacy in various government departments like Defense, Railways etc.</li>
                        <li>Be entrepreneur and start their own retail drug store. </li>
                        <li>Pharmaceutical companies at manufacturing, process control, quality control and marketing. </li>
                        <li>Medical representative. </li>
                        <li>Higher studies (B Pharm, Pharm D, M.Pharm. etc.)</li>
                     </ul>
                     
                     <strong class="mt-4">DURATION</strong>
                     <p>The course for D. Pharm shall extend over 2 years consisting of two years, along with inbuilt industrial/ community pharmacy/ hospital training.</p>
                     
                     <strong class="mt-4">ATTENDANCE</strong>
                     <p>A student is expected to attend at least 75 % of total attendance for each subject, in the absence of which he/she will not be eligible to appear the exam for that subject as per PCI and BTE norms. A student is required to monitor his/her attendance and no separate notice will be displayed.</p>
                     
