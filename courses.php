                  <strong>COURSE DESCRIPTION</strong>
                 <div class="dpharmtextwrap">
                    <strong>PROGRAM INTRODUCTION</strong>
                    <p>This is a 4-year program spread over 8 semesters, leading to a Bachelor's degree in Pharmacy. The program is tailor-made to train the students to play an important role in community pharmacy and assist the pharmaceutical industry at various levels. The course is predominantly based on practice and interaction with a strong focus on the healthcare system. This will enable students to practice pharmacy profession in a strong, skillful and determined manner.</p>
                   
                   <strong class="mt-4">MERITS</strong>
                   <ul class="ullist">
                      <li>Experienced and vibrant full-time faculty</li> 
                      <li>Electives in tune with specializations at undergraduate level</li>            
                      <li>Regular interaction with community pharmacy and industrial pharmacy experts</li>
                      <li>Opportunity to use and witness postgraduate and research infrastructure at the undergraduate level</li>
                      <li>Guidance and counseling from faculty members</li>
                      <li>Placement focus in Pharmaceutical industries, Hospitals, Patent cells, IPR sections, Clinical Research Organizations, Retail Pharmacy and other opportunities</li>
                   </ul>
                   
                   <strong class="mt-4">CAREER OPTIONS</strong>
                   <ul class="ullist">
                   	   <li>Production &amp; Manufacturing</li>
                       <li>Research &amp; Development</li>
                       <li>Analysis &amp; Testing</li>
                       <li>Hospital Pharmacy</li>
                       <li>Regulatory Affairs</li>
                       <li>Drug Inspection</li>
                       <li>Marketing</li>
                       <li>Academics</li>
                       <li>Consultancy</li>
                       <li>Community Pharmacy</li>
                       <li>Opportunities Abroad</li>
                       <li>Clinical Trails</li>
                       <li>Documentation, Library Information Services &amp; Pharm Journalism</li>
                      </ul>
                   
                   <strong class="mt-4">DURATION</strong>
                   <p>The course for B. Pharm shall extend over four years consisting of two semesters each year, along with inbuilt industrial/ community pharmacy/ hospital training.</p>
                   
